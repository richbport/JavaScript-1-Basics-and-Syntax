// Variables
const welcomeMessage = "Hello"; // string (const)
let userName = "John"; // string (let can change)
let userAge = 20; // number

// Output greeting
console.log(welcomeMessage + ", " + userName + "!"); // string concantenation

// Using operators
let nextYearAge = userAge + 1; // arithmetic operator

// Display age information
console.log("You are " + userAge + " years old.");
console.log("Next year, you will be " + nextYearAge + ".");

// Data type examples
let isAdult = userAge >= 18; // boolean using comparison operator
console.log("Are you an adult? " + isAdult);